    private Boolean checkBlankInput()
    {
        if(pesonNameJTextField.getText().length()==0
            ||personAgeJTextField.getText().length()==0
            ||txtCity.getText().length()==0
            ||txtCommunity.getText().length()==0
            ||txtCountry.getText().length()==0
            ||txtDOB.getText().length()==0
            ||txtEmail.getText().length()==0
            ||txtHouseNo.getText().length()==0
            ||txtPhoneNo.getText().length()==0
            ||txtStName.getText().length()==0
            ||txtState.getText().length()==0
            ||txtZipCode.getText().length()==0
                )
        {
            return false;
        }
        else{
            return true;
        }
    }
