    private void addPersonJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPersonJButtonActionPerformed
        // TODO add your handling code here:                
         PersonDetails person=personDirectory.createAndAddPerson();
        if (checkBlankInput()==Boolean.TRUE)
        {
           
            person.setPersonName(pesonNameJTextField.getText());
            person.setAge(Integer.parseInt(personAgeJTextField.getText()));
            person.setCityName(txtCity.getText());        
            person.setCountry(txtCountry.getText());        
            Date DOB = new Date(txtDOB.getText());                
            person.setDob(DOB);        
            person.setEmail(txtEmail.getText());        
           // person.setPersonId(txtIDProof.getText());        
            person.setState(txtState.getText());        
            person.setStreetName(txtStName.getText());        
           // person.setZipCode(Integer.valueOf(txtZipCode.getText()));
             person.setZipCode(parseInt(txtZipCode.getText()));
            person.setPhoneNo(txtPhoneNo.getText());
            person.setCommunityName(txtCommunity.getText());        
            person.setHouseNo(parseInt(txtHouseNo.getText()));
            JOptionPane.showMessageDialog(this, "Person added!!", "Update",
                    JOptionPane.INFORMATION_MESSAGE);            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please enter all values",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        /*Validation to check if name is only alphabetic*/
        if(pesonNameJTextField.getText()!=null){
            Pattern p = Pattern.compile("[^a-z]", Pattern.CASE_INSENSITIVE);
            Matcher m_1 = p.matcher(pesonNameJTextField.getText());
            boolean b_1 = m_1.find();
          // System.out.println(b_1);
        
                if(!b_1){
                    person.setName(pesonNameJTextField.getText());
                }
        
                else
                {
                    JOptionPane.showMessageDialog(this, "Please enter a valid user name");                
                    return;
                }
        }
        /*Validation to check if mobile number is of 10 digits*/
        if(!txtPhoneNo.getText().isBlank())
        if(String.valueOf(Long.parseLong(txtPhoneNo.getText().trim())).length()!=10){
            JOptionPane.showMessageDialog(this, "Telephone number should be 10 digits");
            return;
        }                        
        /*Validation to check if email id is in appropriate format */
        Pattern p_mail = Pattern.compile("^[a-zA-Z0-9]+@[a-z]*.com");
        Matcher m_mail = p_mail.matcher(txtEmail.getText());
        if (! m_mail.matches())
        {
            JOptionPane.showMessageDialog(txtEmail, "Invalid Mail Address. Please enter valid email id ");
            return;
        }
        else
        {
            person.setEmail(txtEmail.getText());
        }
        clearFields();
    }//GEN-LAST:event_addPersonJButtonActionPerformed
